public struct EffectConfig
{
    public string effectText;      // 文本描述
    public string timing;          // 触发时机
    public string condition;       // 条件

    public string target;          // 作用目标
    public string effect;          // 效果名/主效果
    public string effectType;      // 效果类型

    public string effectDetail;    // 效果细节
    public string effectDuration;  // 持续时间

    public string cost_type;       // 费用类型（如MP/能量/次数）
    public string cost_target;     // 费用作用对象/来源

    public string extra;
}